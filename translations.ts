/**
 * Robust translator: Translates any string word-by-word or phrase-by-phrase for ALL supported languages.
 */
export function translateText(text: string, langId: string): string {
  if (!text || !langId || langId.startsWith('en')) {
    return text;
  }

  const clean = text.trim();

  // 1. Direct key match in default translations
  if (TRANSLATIONS[langId]?.[clean]) {
    return TRANSLATIONS[langId][clean];
  }

  // 2. Direct exact phrase mapping
  if (DICTIONARY[langId]?.[clean]) {
    return DICTIONARY[langId][clean];
  }

  const cleanLower = clean.toLowerCase();
  
  // 3. Lowercase exact phrase mapping
  if (DICTIONARY[langId]?.[cleanLower]) {
    return DICTIONARY[langId][cleanLower];
  }

  // 4. Word-by-word recursive translation to translate literally EVERYTHING on screen!
  // Splits text by spacing, slashes, punctuation, parenthesis, so structures are kept intact.
  const regex = /(\s+|[,.!?:;'"()\[\]{}/\\&~·✓•●]+)/g;
  const parts = clean.split(regex);
  const translatedParts = parts.map(part => {
    if (!part || /^\s+$/.test(part) || /^[,.!?:;'"()\[\]{}/\\&~·✓•●]+$/.test(part)) {
      return part; // Return spacing or symbols unchanged
    }

    const partLower = part.toLowerCase();
    const wordDict = WORD_DICTIONARY[langId];

    if (wordDict && wordDict[partLower]) {
      const translatedWord = wordDict[partLower];
      
      // Attempt casing similarity
      if (part[0] === part[0].toUpperCase()) {
        if (part === part.toUpperCase() && part.length > 1) {
          return translatedWord.toUpperCase();
        }
        return translatedWord.charAt(0).toUpperCase() + translatedWord.slice(1);
      }
      return translatedWord;
    }
    
    return part; // fallback to original word/id
  });

  return translatedParts.join('');
}
